#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;

	if (!(new = malloc(sizeof(*src))))
		return (0);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
